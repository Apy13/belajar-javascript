// operator aritmatika

// ekspresi (expression) dan statement
// % <- modulo
// ++ <- increment
// -- <- decrement
// ** <- paangkat

let x = 10;
console.log(x + 1);
console.log(x - 1);
console.log(x / 4);
console.log(x * 2);

// aritmatika khusus di javascript

console.log(x % 4); // sisa bagi
console.log(x ** 2); // pangkat

// pemograman biasa 
x = x + 1;
console.log(x);
x = x + 1;
console.log(x);
x++;
x++;
console.log(x);

x = x - 1;
console.log(x);
x--;
console.log(x);

